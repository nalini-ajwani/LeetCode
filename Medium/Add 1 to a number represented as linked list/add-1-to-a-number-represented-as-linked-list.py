class Solution:
    def addOne(self,head):
        #Returns new head of linked List.
        def rev(head):
            prev = None
            curr = head
            while curr:
                nn = curr.next
                curr.next = prev
                prev = curr
                curr = nn
            return prev
        head = rev(head)
        
        curr = head
        carry = 1
        while curr:
            curr.data += carry
            if curr.data < 10:
                carry = 0
                break
            else:
                curr.data = 0
                carry = 1
                
            if curr.next is None and carry:
                curr.next = Node(0)
            curr = curr.next
        head = rev(head)
        return head
                
            

