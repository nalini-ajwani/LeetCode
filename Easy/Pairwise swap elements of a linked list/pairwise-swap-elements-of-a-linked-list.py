# return head of list after swapping
class Solution:    
    def pairWiseSwap(self, head):
        # code here
        if head is None or head.next is None:
            return head
        
        # Initialize previous and current pointers
        prev = head
        current = head.next
        
        # Change head to the second node
        head = current
        
        # Traverse the linked list and swap nodes pairwise
        while True:
            next_ = current.next
            current.next = prev  # Swap the nodes
            if next_ is None or next_.next is None:
                prev.next = next_
                break
            # Connect the previous node to the next pair
            prev.next = next_.next
            # Move to the next pair
            prev = next_
            current = prev.next
            
        return head


